def _generate_template_interpretation(
    role_key: str,
    hex_number: int,
    hex_name: str,
    core_meaning: str,
) -> RoleInterpretation:
    """Generate a template-based role interpretation from hexagram data."""
    ctx = _ROLE_CONTEXT[role_key]
    actor = ctx["actor"]
    domain = ctx["domain"]

    # Extract first sentence of core_meaning for bridge
    first_sentence = core_meaning.split("。")[0] + "。" if "。" in core_meaning else core_meaning[:80]

    core_bridge = (
        f"{hex_name}卦的核心是{first_sentence}"
        f"对{actor}来说，这映射到{domain}中的一个关键态势：你需要用{hex_name}卦的视角"
        f"重新审视当前的处境，看看卦象揭示了哪些你可能忽略的维度。"
    )

    action = (
        f"以{hex_name}卦的智慧为指引，审视你在{domain}中的核心挑战。"
        f"结合卦象的策略方向来调整你的下一步行动。"
    )

    warning = f"不要机械套用卦象——{hex_name}卦的智慧需要结合你{domain}中的具体情境来理解。"

    return RoleInterpretation(
        core_bridge=core_bridge,
        action=action,
        warning=warning,
        is_template=True,
    )


def get_role_interpretation(role_key: str, hex_number: int) -> RoleInterpretation:
    """Get role-specific interpretation for a hexagram.

    Returns hand-written interpretation if available,
    otherwise generates a template-based one.
    """
    role_interps = ROLE_INTERPRETATIONS.get(role_key, {})

    # Return hand-written if exists
    if hex_number in role_interps:
        return role_interps[hex_number]

    # Generate template-based interpretation
    from app.data.hexagrams import HEXAGRAM_DETAILS
    hex_data = HEXAGRAM_DETAILS.get(hex_number)
    if hex_data:
        return _generate_template_interpretation(
            role_key=role_key,
            hex_number=hex_number,
            hex_name=hex_data.name,
            core_meaning=hex_data.core_meaning,
        )

    # Fallback
    return RoleInterpretation(
        core_bridge="此卦的角色解读正在完善中。请结合卦象的通用含义来理解。",
        action="结合卦象的策略建议，审视你在当前角色中的处境。",
        warning="通用解读仅供参考，具体情境需要具体分析。",
        is_template=True,
    )
