"""爻辞数据库：64卦的六爻数据。"""

from dataclasses import dataclass


@dataclass
class YaoLine:
    position: int       # 1-6
    is_yang: bool       # True=阳爻(九), False=阴爻(六)
    classical: str      # 爻辞原文
    interpretation: str # 白话解读
    advice: str         # 投资行动建议


# 八卦的二进制线型（从底到顶）
# 用于从上下卦推导六爻的阴阳
# 修正：按易经传统，卦画从下到上
# 乾 ☰ = 阳阳阳 (111)
# 坤 ☷ = 阴阴阴 (000)
# 震 ☳ = 阳阴阴 (100) — 初爻阳
# 坎 ☵ = 阴阳阴 (010) — 中爻阳
# 艮 ☶ = 阴阴阳 (001) — 上爻阳
# 巽 ☴ = 阴阳阳 (011) — 初爻阴 (与震相反)
# 离 ☲ = 阳阴阳 (101) — 中爻阴 (与坎相反)
# 兑 ☱ = 阳阳阴 (110) — 上爻阴 (与艮相反)

TRIGRAM_LINES: dict[str, tuple[bool, bool, bool]] = {
    "qian": (True, True, True),
    "kun":  (False, False, False),
    "zhen": (True, False, False),
    "kan":  (False, True, False),
    "gen":  (False, False, True),
    "xun":  (False, True, True),
    "li":   (True, False, True),
    "dui":  (True, True, False),
}


def get_hexagram_lines(lower_key: str, upper_key: str) -> list[bool]:
    """返回六爻的阴阳列表（从初爻到上爻）。"""
    lower = TRIGRAM_LINES[lower_key]
    upper = TRIGRAM_LINES[upper_key]
    return [lower[0], lower[1], lower[2], upper[0], upper[1], upper[2]]


def _yao_name(position: int, is_yang: bool) -> str:
    """返回爻名，如'初九'、'六二'等。"""
    pos_names = {1: "初", 2: "二", 3: "三", 4: "四", 5: "五", 6: "上"}
    if position == 1:
        return f"初{'九' if is_yang else '六'}"
    elif position == 6:
        return f"上{'九' if is_yang else '六'}"
    else:
        return f"{'九' if is_yang else '六'}{pos_names[position]}"


