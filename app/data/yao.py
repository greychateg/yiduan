def get_stage_labels(hex_number: int, lower_key: str, upper_key: str) -> list[str]:
    """返回指定卦的6个阶段简短标签。"""
    if hex_number in HEXAGRAM_STAGES:
        return HEXAGRAM_STAGES[hex_number]

    # 对未手写标签的卦，从爻辞生成
    if hex_number in DETAILED_YAO:
        labels = []
        for yao in DETAILED_YAO[hex_number]:
            text = yao.classical
            # 取前4个字作为标签
            label = text[:4] if len(text) >= 4 else text
            labels.append(label)
        return labels

    # 最后回退：基于位置的通用标签 + 卦名
    from app.data.hexagrams import HEXAGRAM_DETAILS
    name = ""
    if hex_number in HEXAGRAM_DETAILS:
        name = HEXAGRAM_DETAILS[hex_number].name
    return [
        f"{name}之初",
        f"{name}之中",
        f"{name}之危",
        f"{name}之变",
        f"{name}之盛",
        f"{name}之终",
    ]


