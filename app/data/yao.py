def _get_hexagram_context(hex_number: int) -> tuple[str, bool]:
    """获取卦的上下文信息。返回 (卦名描述, 是否积极卦)。"""
    from app.data.hexagrams import HEXAGRAM_DETAILS
    if hex_number in HEXAGRAM_DETAILS:
        h = HEXAGRAM_DETAILS[hex_number]
        return h.full_name, hex_number not in {4, 12, 23, 29, 36, 39, 47, 64}
    return f"第{hex_number}卦", True


