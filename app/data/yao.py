def _generate_structural_yao(hex_number: int, lines: list[bool]) -> list[YaoLine]:
    """为非详细卦生成基于结构、卦义和位置的爻辞。"""
    from app.data.hexagrams import HEXAGRAM_DETAILS

    # 获取卦的详细信息
    hex_name = f"第{hex_number}卦"
    core_meaning = ""
    strategy = ""
    situation = ""
    gua_name = ""
    is_positive = True

    if hex_number in HEXAGRAM_DETAILS:
        h = HEXAGRAM_DETAILS[hex_number]
        hex_name = h.full_name
        core_meaning = h.core_meaning
        strategy = h.strategy
        situation = h.situation
        gua_name = h.name
        is_positive = hex_number not in {4, 12, 23, 29, 36, 39, 47, 64}

    # 应爻关系: 1↔4, 2↔5, 3↔6
    yingyao = {1: 4, 2: 5, 3: 6, 4: 1, 5: 2, 6: 3}

    result = []
    for i, is_yang in enumerate(lines):
        pos = i + 1
        name = _yao_name(pos, is_yang)

        # 当位判断
        is_proper = (is_yang and pos % 2 == 1) or (not is_yang and pos % 2 == 0)

        # 得中判断
        is_center = pos in (2, 5)

        # 应爻关系
        partner_pos = yingyao[pos]
        partner_yang = lines[partner_pos - 1]
        has_response = is_yang != partner_yang

        # 构建 classical 文本
        yang_yin = "阳" if is_yang else "阴"
        stage_name, stage_desc = _POS_STAGE[pos]

        proper_text = "当位" if is_proper else "不当位"
        center_text = "得中" if is_center else ""
        status_parts = [f"{yang_yin}爻{proper_text}"]
        if center_text:
            status_parts.append(center_text)

        response_text = f"与第{partner_pos}爻相应" if has_response else f"与第{partner_pos}爻不应"
        status_parts.append(response_text)

        classical = f"{name}——{gua_name}卦第{pos}爻（{'、'.join(status_parts)}）"

        # 构建 interpretation
        # 结合卦义和位置含义
        if core_meaning:
            # 取卦义的第一句话
            core_first = core_meaning.split("。")[0] + "。"
        else:
            core_first = ""

        if is_proper and is_center:
            quality = "位置极佳——当位得中，内外兼备"
        elif is_proper:
            quality = "当位——力量与位置匹配"
        elif is_center:
            quality = "虽不当位但得中——中正之德可补位之不足"
        else:
            quality = "不当位——需要调整姿态来适应"

        interp_parts = [
            f"{hex_name}的{stage_name}阶段。{stage_desc}",
            f"{core_first}" if core_first else "",
            f"此爻{quality}。",
        ]
        if has_response:
            interp_parts.append(f"与第{partner_pos}爻阴阳相应，有外部助力。")
        else:
            interp_parts.append(f"与第{partner_pos}爻同性不应，需要自力更生。")

        interpretation = "".join(p for p in interp_parts if p)

        # 构建 advice
        if is_yang:
            base_advice = _POS_CAUTION_YANG[pos]
        else:
            base_advice = _POS_CAUTION_YIN[pos]

        # 叠加卦义的策略信息
        if strategy and pos in (2, 5):
            strategy_first = strategy.split("。")[0] + "。"
            advice = f"{base_advice} {strategy_first}"
        elif strategy and pos == 3:
            advice = f"{base_advice} 特别注意：这是{gua_name}卦最容易出错的阶段。"
        elif strategy and pos == 6:
            advice = f"{base_advice} {gua_name}卦到此该收场了。"
        else:
            advice = base_advice

        result.append(YaoLine(
            position=pos,
            is_yang=is_yang,
            classical=classical,
            interpretation=interpretation,
            advice=advice,
        ))

    return result


def get_yao_lines(hex_number: int, lower_key: str, upper_key: str) -> list[YaoLine]:
    """获取指定卦的六爻数据。"""
    if hex_number in DETAILED_YAO:
        return DETAILED_YAO[hex_number]

    lines = get_hexagram_lines(lower_key, upper_key)
    return _generate_structural_yao(hex_number, lines)
