@dataclass
class RoleQuestion:
    title: str
    options: list[dict]  # 4 items, each: {key, label, description}


@dataclass
class Role:
    key: str
    name: str
    icon: str
    subtitle: str
    questions: list[RoleQuestion] = field(default_factory=list)
    blind_spots: list[str] = field(default_factory=list)


def _q(title: str, yy: str, oy: str, yi: str, oi: str) -> RoleQuestion:
    """Helper: build a RoleQuestion with 4 options."""
    return RoleQuestion(
        title=title,
        options=[
            {"key": "young_yang", "label": "少阳", "description": yy},
            {"key": "old_yang", "label": "太阳", "description": oy},
            {"key": "young_yin", "label": "少阴", "description": yi},
            {"key": "old_yin", "label": "太阴", "description": oi},
        ],
    )


